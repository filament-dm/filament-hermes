logger = logging.getLogger("gateway.filament_fcm")

_DEFAULT_MCP_URL = "https://api.filament.dm/mcp/agents"

# ── Tool list ──────────────────────────────────────────────────────
#
# The tool list is fetched dynamically from the MCP server (tools/list)
# at registration time — see ``_resolve_tools``. ``tool_manifest.json``
# (generated by ``filament mcp dump-tools``) is kept as a static
# fallback for when the server is unreachable, no token is configured,
# or the response is malformed.

_MANIFEST_PATH = Path(__file__).parent / "tool_manifest.json"

with open(_MANIFEST_PATH) as _f:
    _STATIC_TOOLS: list[dict] = json.load(_f)


def _resolve_tools(mcp_url: str, mcp_token: str) -> list[dict]:
    """Return the tool definitions to register.

    Fetches the live list from the MCP server when a token is available,
    falling back to the bundled static manifest on any failure (missing
    token, network error, malformed response). This keeps offline setup
    and degraded-network startup working.
    """
    if mcp_token:
        try:
            tools = FilamentAPI.fetch_tools(mcp_url, mcp_token)
            logger.info("filament-fcm: fetched %d tools from MCP server", len(tools))
            return tools
        except Exception as exc:
            logger.warning(
                "filament-fcm: dynamic tool fetch failed (%s); "
                "falling back to static manifest",
                exc,
            )
    return _STATIC_TOOLS


# Tools NOT exposed to the LLM.  Each entry documents why the tool is
