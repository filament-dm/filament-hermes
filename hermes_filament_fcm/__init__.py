"""Filament FCM — Hermes gateway adapter via FCM push notifications.

Distributed as a pip package with firebase-messaging as a dependency.
Auto-discovered by Hermes via the ``hermes_agent.plugins`` entry-point.

Required environment variable:
  FILAMENT_MCP_TOKEN          — a pre-generated MCP agent token

Optional environment variables:
  FILAMENT_CONTROL_USERS    — comma-separated Filament user IDs that form the
                                *control plane* (trusted commanders) alongside
                                the principal (owner), who is auto-discovered
                                from the MCP ``get_self`` tool at startup.
  FILAMENT_ALLOW_DATA_USERS    — admit untrusted participants (the *data plane*)
                                in whatever loops the agent is in; their messages
                                are framed as data, not commands. Defaults to
                                true (public). Set false for a control-plane-only
                                (owner/trusted) agent.

See adapter.py and docs/agent-boundaries.md for the trust-zone ("Warden") model.

See README.md for how to generate a token using the RFC 8693 token
exchange endpoint.
"""

